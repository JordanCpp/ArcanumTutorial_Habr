#ifndef Pollux_LiteCpp_vector_hpp
#define Pollux_LiteCpp_vector_hpp

#include <Pollux/LiteCpp/memory_resource.hpp>
#include <Pollux/Common/Assert.hpp>

namespace std
{
	namespace pmr
	{
		template <typename T>
		class vector
		{
		public:
			vector(memory_resource* source) :
				_Capacity(0),
				_Position(0),
				_Content(NULL),
				_Memory(source)
			{
			}

			~vector()
			{
				_Content = NULL;
				_Memory  = NULL;
			}

			size_t capacity() const
			{
				return _Capacity;
			}

			size_t size() const
			{
				return _Position;
			}

			const T* data() const
			{
				return _Content;
			}

			T* data()
			{
				return _Content;
			}

			void reserve(size_t count)
			{
				if (count > _Capacity)
				{
					size_t allocatedSize = count * sizeof(T);

					void* allocatedPtr = _Memory->allocate(allocatedSize);

					T* p = new (allocatedPtr) T[count];

					for (size_t i = 0; i < _Position; i++)
					{
						p[i] = _Content[i];
					}

					if (_Content != NULL)
					{
						_Memory->deallocate(_Content, _Capacity * sizeof(T));
					}

					_Content  = p;
					_Capacity = count;
				}
			}

			void resize(size_t count)
			{
				if (_Capacity < count)
				{
					reserve(count);
				}

				_Position = count;
			}

			void clear()
			{
				_Position = 0;
			}

			void push_back(const T& element)
			{
				if (_Capacity == 0)
				{
					reserve(2);
				}
				else if (_Position + 1 >= _Capacity)
				{
					reserve(_Capacity * 2);
				}

				_Content[_Position] = element;

				_Position++;
			}

			const T& at(size_t index) const
			{
				POLLUX_ASSERT(index <= _Position);

				return _Content[index];
			}

			const T& operator[] (size_t index) const
			{ 
				POLLUX_ASSERT(index <= _Position);

				return _Content[index];
			}

			T& operator[] (size_t index)
			{
				POLLUX_ASSERT(index <= _Position);

				return _Content[index];
			}

		private:
			size_t           _Capacity;
			size_t           _Position;
			T*               _Content;
			memory_resource* _Memory;
		};
	}
}

#endif 
